from qa_engine import run_engine

def evaluate(test_set):
    total = len(test_set)
    domain_correct = 0
    subcontext_correct = 0
    subcontext_total = 0
    correct_confidences = []
    incorrect_confidences = []

    from collections import defaultdict

    domain_stats = defaultdict(lambda: {"correct": 0, "total": 0})

    print("\n===== NCAA QA ENGINE EVALUATION =====\n")

    for i, item in enumerate(test_set, 1):
        question = item["question"]
        expected_domain = item["expected_domain"]
        expected_subcontext = item.get("expected_subcontext", None)

        result = run_engine(question)

        print(f"Test {i}: {question}")
        print(f"Expected Domain: {expected_domain}")
        print(f"Detected Domain: {result['detected_domain']}")

        # Track per-domain statistics
        domain_stats[expected_domain]["total"] += 1

        is_correct = result["detected_domain"] == expected_domain
        if is_correct:
            domain_stats[expected_domain]["correct"] += 1
            print("Domain: PASS")
            domain_correct += 1
            if result["confidence"] is not None:
                correct_confidences.append(result["confidence"])
        else:
            print("Domain: FAIL")
            if result["confidence"] is not None:
                incorrect_confidences.append(result["confidence"])

        if expected_subcontext:
            print(f"Expected Subcontext: {expected_subcontext}")
            print(f"Detected Subcontext: {result['subcontext']}")

            if result['subcontext'] == expected_subcontext:
                print("Subcontext: PASS")
                subcontext_correct += 1
            else:
                print("Subcontext: FAIL")

            subcontext_total += 1

        print(f"Confidence: {result['confidence']}")
        print("-" * 50)

    print("\n===== SUMMARY =====")
    print(f"Domain Accuracy: {domain_correct}/{total} ({domain_correct/total:.2%})")

    if subcontext_total > 0:
        print(f"Subcontext Accuracy: {subcontext_correct}/{subcontext_total} ({subcontext_correct/subcontext_total:.2%})")

    print("\n===== DOMAIN BREAKDOWN =====")

    for domain, stats in domain_stats.items():
        total_domain = stats["total"]
        correct_domain = stats["correct"]

        accuracy = (correct_domain / total_domain) * 100 if total_domain > 0 else 0

        print(f"{domain}: {correct_domain}/{total_domain} ({accuracy:.1f}%)")

    print("\n===== CONFIDENCE CALIBRATION =====")

    if correct_confidences:
        avg_correct_conf = sum(correct_confidences) / len(correct_confidences)
        print(f"Average Confidence (Correct Predictions): {avg_correct_conf:.2f}")

    if incorrect_confidences:
        avg_incorrect_conf = sum(incorrect_confidences) / len(incorrect_confidences)
        print(f"Average Confidence (Incorrect Predictions): {avg_incorrect_conf:.2f}")

    high_confidence_errors = [c for c in incorrect_confidences if c >= 0.75]
    if high_confidence_errors:
        print(f"High Confidence Errors (>= 0.75): {len(high_confidence_errors)}")
    else:
        print("No High Confidence Errors Detected")


if __name__ == "__main__":
    from evaluation import golden_questions
    evaluate(golden_questions)